//! Browser-OAuth for Apple + Google — the relay-mediated path.
//!
//! ## Flow
//!
//! ```text
//!  Desktop                  Browser                  API                Provider
//!     │                        │                       │                    │
//!     │ 1. open URL ──────────▶│                       │                    │
//!     │                        │ 2. GET /…/start ─────▶│                    │
//!     │                        │                       │ 3. 302 to ─────────▶
//!     │                        │                       │     provider auth  │
//!     │                        │ 4. user authenticates ─────────────────────│
//!     │                        │ 5. redirect ──────────│◀───────────────────│
//!     │                        │                       │     /…/callback?…  │
//!     │                        │                       │ 6. exchange code   │
//!     │                        │                       │     ──────────────▶│
//!     │                        │                       │ 7. ◀── id_token   │
//!     │                        │                       │ 8. mint Libre │
//!     │                        │                       │     token          │
//!     │                        │ 9. ◀── 302 ────────── │                    │
//!     │                        │     libre://oauth/done?…              │
//!     │ 10. deep-link ────────│                       │                    │
//!     │     captures token     │                       │                    │
//! ```
//!
//! The `state` parameter does double duty: a CSRF nonce that protects
//! the callback from replay, and a key into a server-side cache that
//! holds the `code_verifier` (PKCE) plus the desktop's `session` id
//! (so the deep-link callback knows which window to wake up — useful
//! when the user has multiple Libre instances open).
//!
//! The `session` id is generated by the desktop, passed through as a
//! query param on the start URL, round-tripped through the provider,
//! and echoed back to the desktop on the deep-link callback. The
//! desktop's `tauri-plugin-deep-link` handler matches on it.
//!
//! ## State cache
//!
//! In-memory `DashMap<state, OauthSessionEntry>` with a 10-minute
//! sweep. Restart wipes the cache, which is fine — the worst case is
//! a user who got distracted mid-auth has to click the button again.

use axum::{
    extract::{Form, Query, State},
    http::StatusCode,
    response::{IntoResponse, Redirect, Response},
};
use base64::Engine;
use dashmap::DashMap;
use jsonwebtoken::{EncodingKey, Header};
use once_cell::sync::Lazy;
use rand::RngCore;
use serde::{Deserialize, Serialize};
use sha2::{Digest, Sha256};
use std::sync::Arc;
use std::time::{Duration, Instant};

use super::oauth as token_verify;
use crate::auth::hash_token;
use crate::state::AppState;

const STATE_TTL: Duration = Duration::from_secs(10 * 60);

#[derive(Debug, Clone)]
struct OauthSessionEntry {
    /// Provider name — keeps the callback honest (a Google `code`
    /// posted to the Apple route gets rejected before we try to
    /// exchange).
    provider: Provider,
    /// PKCE verifier the desktop generated. We keep it server-side so
    /// the redirect URL can stay short and the user can't tamper with
    /// it via browser DevTools.
    code_verifier: String,
    /// Per-window id chosen by the desktop. Echoed back via the
    /// deep-link callback so multi-instance setups don't cross-wire.
    session: String,
    /// Display-name hint the desktop optionally passed in. Some
    /// flows (Apple) only return the user's name on first sign-in;
    /// the desktop UI lets the user fill it in if they want.
    display_name: Option<String>,
    /// Custom-scheme URL the desktop wants the browser to redirect
    /// back to once auth completes. Validated against an allow-list
    /// (must be `libre://` or `kata://`) on use.
    return_to: Option<String>,
    expires_at: Instant,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum Provider {
    Apple,
    Google,
}

/// Process-global cache. `OnceCell::Lazy` rather than `AppState`
/// because we don't want to plumb a new field through everywhere — it
/// truly is a singleton, scoped to the API process lifetime.
static OAUTH_STATES: Lazy<DashMap<String, OauthSessionEntry>> =
    Lazy::new(DashMap::new);

fn random_url_safe(len_bytes: usize) -> String {
    let mut bytes = vec![0u8; len_bytes];
    rand::thread_rng().fill_bytes(&mut bytes);
    base64::engine::general_purpose::URL_SAFE_NO_PAD.encode(&bytes)
}

fn pkce_challenge(verifier: &str) -> String {
    let mut hasher = Sha256::new();
    hasher.update(verifier.as_bytes());
    base64::engine::general_purpose::URL_SAFE_NO_PAD.encode(hasher.finalize())
}

fn sweep() {
    let now = Instant::now();
    OAUTH_STATES.retain(|_, v| v.expires_at > now);
}

// ── Start endpoints ─────────────────────────────────────────────

#[derive(Deserialize)]
pub struct StartParams {
    /// Per-window id chosen by the desktop. Round-tripped through the
    /// provider and echoed back on the deep-link callback.
    pub session: String,
    /// Optional pre-filled display name (Apple sometimes returns the
    /// name only on first sign-in — desktop can let the user type it
    /// in advance).
    pub display_name: Option<String>,
    /// The custom URL scheme the desktop registered. Defaults to
    /// `libre://oauth/done` if omitted; tests can override it.
    pub return_to: Option<String>,
}

/// HTTPS return URLs we trust to receive a freshly-minted token.
/// The web build of Libre can't follow a `libre://` deep-link,
/// so its OAuth popup redirects through a same-origin `/oauth/done`
/// page that postMessages the token back to the parent. Any URL not
/// in this list (or matching the `libre://` / `kata://` scheme
/// allowlist below) falls through to the default `libre://oauth/done`,
/// which a browser can't follow — so an attacker who got the user
/// to click `return_to=https://attacker.com/log?token=` simply ends
/// up with a dead link in their popup, never the bearer.
///
/// Add new origins explicitly. Don't widen this to a wildcard.
const WEB_RETURN_ALLOWLIST: &[&str] = &[
    "https://libre.academy/oauth/done",
];

/// Build the deep-link return URL the desktop should be redirected
/// to once auth completes. Always passes through this single helper
/// so a redirect injection (`return_to=https://attacker.com`) gets
/// caught here — we only allow the configured scheme prefix or an
/// exact origin from `WEB_RETURN_ALLOWLIST`.
fn build_return_url(
    desired: Option<&str>,
    session: &str,
    payload: ReturnPayload,
) -> String {
    let base = desired.unwrap_or("libre://oauth/done");
    // Reject any return_to that isn't either:
    //   - a custom-scheme URL the desktop / iOS app handles, or
    //   - an exact-match entry from WEB_RETURN_ALLOWLIST (the
    //     libre.academy `/oauth/done` page that postMessages
    //     the token back to the popup opener).
    // The browser-OAuth handoff is the only sanctioned shape, and an
    // attacker who got the user to click a malicious link with
    // `return_to=https://attacker.com/log?libre_token=` would
    // otherwise leak the bearer.
    // `libre-dev://` is the desktop dev-build twin of `libre://`.
    // macOS Launch Services routes each custom scheme to whichever
    // `.app` bundle most recently registered it, and the installed
    // prod app re-claims `libre://` on every launch — meaning a
    // `tauri dev` session's OAuth callback gets hijacked by the prod
    // install. Splitting the scheme keeps dev callbacks landing in
    // the dev binary. The desktop config registers both schemes so
    // each build's Launch Services entry covers its own variant.
    let safe_base = if base.starts_with("libre://")
        || base.starts_with("libre-dev://")
        || base.starts_with("kata://")
        || WEB_RETURN_ALLOWLIST.iter().any(|allowed| base == *allowed)
    {
        base
    } else {
        "libre://oauth/done"
    };
    let mut url = url::Url::parse(safe_base).expect("valid scheme");
    {
        let mut q = url.query_pairs_mut();
        q.append_pair("session", session);
        match payload {
            ReturnPayload::Success { token } => {
                q.append_pair("status", "ok");
                q.append_pair("token", &token);
            }
            ReturnPayload::Error { code, message } => {
                q.append_pair("status", "error");
                q.append_pair("error", code);
                q.append_pair("message", message);
            }
        }
    }
    url.into()
}

enum ReturnPayload<'a> {
    Success { token: String },
    Error { code: &'a str, message: &'a str },
}

pub async fn google_start(
    State(state): State<Arc<AppState>>,
    Query(params): Query<StartParams>,
) -> Result<Redirect, StatusCode> {
    sweep();
    let client_id = state
        .google_audience
        .as_ref()
        .ok_or(StatusCode::SERVICE_UNAVAILABLE)?;
    let redirect_uri = state
        .public_url
        .as_ref()
        .map(|u| format!("{}/libre/auth/google/callback", u.trim_end_matches('/')))
        .ok_or(StatusCode::SERVICE_UNAVAILABLE)?;

    let csrf = random_url_safe(24);
    let verifier = random_url_safe(48);
    let challenge = pkce_challenge(&verifier);

    OAUTH_STATES.insert(
        csrf.clone(),
        OauthSessionEntry {
            provider: Provider::Google,
            code_verifier: verifier,
            session: params.session.clone(),
            display_name: params.display_name.clone(),
            return_to: params.return_to.clone(),
            expires_at: Instant::now() + STATE_TTL,
        },
    );

    let mut auth = url::Url::parse("https://accounts.google.com/o/oauth2/v2/auth")
        .expect("static URL");
    {
        let mut q = auth.query_pairs_mut();
        q.append_pair("client_id", client_id);
        q.append_pair("redirect_uri", &redirect_uri);
        q.append_pair("response_type", "code");
        q.append_pair("scope", "openid email profile");
        q.append_pair("state", &csrf);
        q.append_pair("code_challenge", &challenge);
        q.append_pair("code_challenge_method", "S256");
        // `prompt=select_account` lets users pick a fresh account
        // without first signing out of every Google session — common
        // request when sharing a machine.
        q.append_pair("prompt", "select_account");
    }
    Ok(Redirect::to(auth.as_str()))
}

pub async fn apple_start(
    State(state): State<Arc<AppState>>,
    Query(params): Query<StartParams>,
) -> Result<Redirect, StatusCode> {
    sweep();
    let client_id = state
        .apple_audience
        .as_ref()
        .ok_or(StatusCode::SERVICE_UNAVAILABLE)?;
    let redirect_uri = state
        .public_url
        .as_ref()
        .map(|u| format!("{}/libre/auth/apple/callback", u.trim_end_matches('/')))
        .ok_or(StatusCode::SERVICE_UNAVAILABLE)?;

    let csrf = random_url_safe(24);
    OAUTH_STATES.insert(
        csrf.clone(),
        OauthSessionEntry {
            provider: Provider::Apple,
            // Apple's web-flow doesn't use PKCE — leave verifier
            // empty; the lookup is by `state` key alone.
            code_verifier: String::new(),
            session: params.session.clone(),
            display_name: params.display_name.clone(),
            return_to: params.return_to.clone(),
            expires_at: Instant::now() + STATE_TTL,
        },
    );

    let mut auth = url::Url::parse("https://appleid.apple.com/auth/authorize")
        .expect("static URL");
    {
        let mut q = auth.query_pairs_mut();
        q.append_pair("client_id", client_id);
        q.append_pair("redirect_uri", &redirect_uri);
        q.append_pair("response_type", "code id_token");
        q.append_pair("scope", "name email");
        q.append_pair("state", &csrf);
        // form_post: Apple POSTs the result as form data instead of a
        // query string — required when the response includes
        // id_token.
        q.append_pair("response_mode", "form_post");
    }
    Ok(Redirect::to(auth.as_str()))
}

